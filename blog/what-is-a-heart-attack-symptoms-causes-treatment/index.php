          <p class="desc-big">What is a Heart Attack? </p>
        <p class="desc-normal">
          A <a href="https://texascardiology.co/services/">heart attack</a> happens when the flow of blood to the heart is stopped or blocked. The stoppage is most usually a build-up of fat, cholesterol, and other substances, which form a plaque in the arteries that feed the heart (coronary arteries).
          <p class="desc-normal">Sometimes, a plaque could rupture and form a clot that stops blood flow. The interrupted blood flow could damage or destroy a portion of the heart muscle.
          <p class="desc-normal">A heart attack, also known as a myocardial infarction, could be lethal, but treatment has improved dramatically over the years. It is important to call 911 or emergency medical help if you think you may be having a heart attack.</p><br>


          <p class="desc-big">Heart Attack Symptoms </p>
          <p class="desc-normal">Common heart attack signs and symptoms involve:<br></p>
          <p class="desc-normal blue_check">
            
            <span>Pressure, tightness, pain, or a squeezing or aching feeling in your chest or arms that might spread to your neck, jaw, or back</span><br />
            <br />
            <span>Nausea, indigestion, heartburn, or abdominal pain</span><br />
            <br />
            <span>Shortness of breath</span><br />
            <br />
            <span>Cold sweat</span><br />
            <br />
            <span>Fatigue</span><br />
            <br />
            <span>Lightheadedness or unexpected dizziness</span>
          </p>

          <br><br>

          <p class="desc-big">Heart attack symptoms vary</p>
        <p class="desc-normal">Not all people who have heart attacks have the same symptoms or have the same seriousness of symptoms. Some people have mild pain; others have more serious pain. Some people have no symptoms. For others, the first sign might be unexpected cardiac arrest. Although, the more signs and symptoms you have, the greater the chance you are having a heart attack.</p>
        <p class="desc-normal">Some heart attacks strike unexpectedly, but many people have warning signs and symptoms hours, days, or weeks in advance. The earliest warning may be recurrent chest pain or pressure (angina) that is triggered by activity and relieved by rest. Angina is caused by a momentary decrease in blood flow to the heart.</p><br><br>


        <img src="../../assets/img/heart-one.jpg" class="img-divider"><br><br>

        <p class="desc-big">When should you see a doctor?</p>
        <p class="desc-normal">Act immediately. Some people wait too long because they do not recognize the crucial signs and symptoms. Take these steps:</p><br>


        <p class="desc-normal blue_check">
            
          <span><b>Call for emergency medical help</b> - If you sense you are having a heart attack, do not hesitate. Instantly call 911 or your local emergency number. If you do not have access or permission to emergency medical services, have someone drive you to the closest hospital.
          Drive yourself only if there is no other choice or alternative. Because your condition could worsen, driving yourself puts you and others at risk.</span><br /><br />

          <span><b>Take nitroglycerin, if prescribed to you by a doctor or primary care physician</b> - Take it as instructed while awaiting emergency help.</span><br /><br />

          <span><b>Take aspirin, if suggested</b> - Taking aspirin during a heart attack can lower heart damage by helping to keep your blood from clotting.</span><br /><br />
        </p>

        <p class="desc-normal">Aspirin could interact with other medications, although, so do not take an aspirin unless your doctor or emergency medical personnel suggest it. Do not postpone calling 911 to take an aspirin. Call for emergency help first.</p><br><br>



        <p class="desc-big">What to do if you see someone who may be having a heart attack</p>
        <p class="desc-normal">If you see someone who is unconscious and you think is having a heart attack, first call for emergency medical help. Then check if the person is breathing and has a heartbeat. If the person is not breathing or you do not find a pulse, only then should you start CPR.</p>
        <p class="desc-normal">Push hard and fast on the person's chest in a fairly quick rhythm about 100 to 120 compressions a minute.</p>
        <p class="desc-normal">If you have not been trained in CPR, primary care physicians suggest performing only chest compressions. If you have been trained in CPR, you could go on to opening the airway and rescue breathing.</p><br><br>

        <img src="../../assets/img/heart-two.jpg" class="img-divider"><br><br>



        <p class="desc-big">Heart Attack Causes</p>
        <p class="desc-normal">A heart attack happens when one or more of your coronary arteries becomes blocked or stopped. Over time, a build-up of fatty deposits, including cholesterol, forms substances known as plaques, which could narrow the arteries (atherosclerosis). This condition is known as coronary artery disease and causes most heart attacks.</p>
        <p class="desc-normal">At the time of a heart attack, a plaque could rupture and spill cholesterol and other substances into the bloodstream. A blood clot forms at the area of the rupture. If the clot is large, it could stop blood flow through the coronary artery, starving the heart of oxygen and nutrients (ischemia).</p>
        <p class="desc-normal">You may have an entire or partial blockage of the coronary artery.</p><br>

        <p class="desc-normal blue_check">
            
          <span>An entire blockage means you have had an ST-elevation myocardial infarction (STEMI).</span><br /><br />

          <span>A partial blocking means you have had a non-ST elevation myocardial infarction (NSTEMI).</span><br /><br />
        </p>
        <p class="desc-normal">Diagnosis and treatment may be different depending on which type you have had.</p>
        <p class="desc-normal">Another cause or reason for a heart attack is a spasm of a coronary artery that shuts down blood flow to a portion of the heart muscle. Using tobacco and illicit drugs, for example, cocaine, could cause a life-threatening spasm.</p>
        <p class="desc-normal">Infection with COVID-19 also might damage your heart in ways that result in a heart attack.</p><br /><br />


        <p class="desc-big">Heart Attack Risk factors</p>
        <p class="desc-normal">Specific factors lead to the unwanted accumulation of fatty deposits (atherosclerosis) that narrows arteries throughout your body. You could improve or eliminate many of these risk factors to lower your chances of having a first or another heart attack.</p><br>
        <p class="desc-normal"><b>Heart attack risk factors include:</b></p>
        <p class="desc-normal blue_check">
            
          <span><b>Age</b> – Men of age 45 years or older and women of age 55 years or older are more likely to have a heart attack than are younger men and women.</span><br /><br />
          <span><b>Tobacco</b> - This involves smoking and long-term exposure to secondhand smoke.</span><br /><br />
          <span><b>High blood pressure</b> - Over time, high blood pressure could damage arteries that lead to your heart. High blood pressure that happens with other conditions, for example, obesity, high cholesterol, or diabetes, increases your risk even more.</span><br /><br />
          <span><b>High blood cholesterol or triglyceride levels</b> - A high level of low-density lipoprotein (LDL) cholesterol ("bad" cholesterol) is most likely to confine arteries. A high level of triglycerides, a type of blood fat associated with your diet, also increases your risk of a heart attack. Although, a high level of high-density lipoprotein (HDL) cholesterol ("good" cholesterol) might lower your risk.</span><br /><br />
          <span><b>Obesity</b> - Obesity is connected with high blood cholesterol levels, high triglyceride levels, high blood pressure, and diabetes. Losing just 10 percent of your body weight could lower this risk.</span><br /><br />
          <span><b>Diabetes</b> - Not producing adequate amounts of a hormone secreted by your pancreas (insulin) or not responding to insulin properly causes your body's blood sugar levels to rise, elevating your risk of a heart attack.</span><br /><br />
          <span><b>Metabolic syndrome</b> - This syndrome happens when you have obesity, high blood pressure, and high blood sugar. Having metabolic syndrome makes you twice as likely to develop heart disease than if you do not have it.</span><br /><br />
          <span><b>Family history of heart attacks</b> - If your siblings, parents, or grandparents have had early heart attacks (by age 55 years for males and by age 65 years for females), you may be at increased risk.</span><br /><br />
          <span><b>Lack of physical activity</b> - Being inactive leads to high blood cholesterol levels and obesity. People who exercise frequently have better heart health, including lower blood pressure.</span><br /><br />
          <span><b>Stress</b> - You may respond to stress in ways that could increase your risk of a heart attack.</span><br /><br />
          <span><b>Illicit drug use</b> - Using stimulant drugs, for example, cocaine or amphetamines, could start a spasm of your coronary arteries that could cause a heart attack.</span><br /><br />
          <span><b>A history of preeclampsia</b> - This condition causes high blood pressure during pregnancy and raises the lifetime risk of heart disease.</span><br /><br />
          <span><b>An autoimmune condition</b> - Having a condition for example rheumatoid arthritis or lupus could increase your risk of a heart attack.</span><br /><br />
        </p><br /><br />



        <p class="desc-big">Heart Attack Complications</p>
        <p class="desc-normal">Complications are usually associated with the damage done to your heart during a heart attack, which could lead to:</p><br>


        <p class="desc-normal blue_check">
            
          <span><b>Abnormal heart rhythms (arrhythmias)</b> - Electrical "short circuits" could develop, resulting in abnormal heart rhythms, some of which could be serious, and might lead to death.</span><br /><br />

          <span><b>Heart failure</b> - A heart attack may damage so much heart tissue that the remaining heart muscle cannot pump enough blood out of your heart. Heart failure could be temporary, or it could be a chronic condition resulting from extensive and permanent damage to your heart.</span><br /><br />

          <span><b>Sudden cardiac arrest</b> - Without warning, your heart stops because of an electrical disturbance that causes an abnormal heart rhythm (arrhythmia). Heart attacks increase the risk of unexpected cardiac arrest, which could cause death without immediate treatment.</span><br /><br />
        </p><br><br>


        <img src="../../assets/img/heart-three.jpg" class="img-divider"><br><br>

        <p class="desc-big">Heart Attack Prevention</p>
        <p class="desc-normal">It is never too late to take steps to prevent or stop a heart attack even if you have already had one. Here are ways to prevent or stop a heart attack.</p><br>


        <p class="desc-normal blue_check">
            
          <span><b>Medications</b> - Taking medications could lower your risk of a subsequent heart attack and help your damaged heart function better. Continue to take what your primary care physician prescribes, and ask your primary care physician how often you require to be monitored.</span><br /><br />

          <span><b>Lifestyle factors</b> - You know the drill: Keep a healthy weight with a heart-healthy diet, do not smoke, exercise regularly, manage stress, and control conditions that could lead to a heart attack, for example, high blood pressure, high cholesterol, and diabetes.</span><br /><br />
        </p><br><br>







        <p class="desc-big">Heart Attack Diagnosis</p>
        <p class="desc-normal">Ideally, your primary care physician should screen you during regular physical examinations for risk factors that could lead to a heart attack.</p>
        <p class="desc-normal">If you are in an emergency setting for symptoms of a heart attack, you will be asked about your symptoms and have your blood pressure, pulse and temperature checked. You will be attached to a heart monitor and have tests to see if you are having a heart attack.</p><br>


        <p class="desc-normal"><b>Tests to diagnose a heart attack include:</b></p>
        <p class="desc-normal blue_check">
            
          <span><b>Electrocardiogram (ECG)</b> - This first test is done to diagnose a heart attack records electrical signals as they travel through your heart. Sticky patches (electrodes) are connected to your chest and limbs. Signals are recorded as waves shown on a monitor or printed on paper. Because injured heart muscle does not conduct electrical impulses normally, the electrocardiogram (ECG) might show that a heart attack has happened or is in progress.</span><br /><br />

          <span><b>Blood tests</b> - Specific heart proteins slowly leak into your blood after heart damage from a heart attack. Emergency room primary care physicians will take samples of your blood to check for these proteins or enzymes.</span><br /><br />
        </p><br><br>



        <p class="desc-big">Additional tests</p>
        <p class="desc-normal">If you have had or are having a heart attack, primary care physicians will take immediate steps to treat your condition. You may also have these additional or extra tests.</p><br>


        <p class="desc-normal blue_check">
            
          <span><b>Chest X-ray</b> - An X-ray image of your chest permits your primary care physician to check the size of your heart and its blood vessels and to look for fluid in your lungs.</span><br /><br />

          <span><b>Echocardiogram</b> - Sound waves (ultrasound) create pictures of the moving heart. Your primary care physician could use this test to see how your heart's chambers and valves are pumping blood through your heart. An echocardiogram could help identify whether an area of your heart has been damaged.</span><br /><br />

          <span><b>Coronary catheterization (angiogram)</b> - A liquid dye is administered or injected into the arteries of your heart through a long, thin tube (catheter) that is fed through an artery, generally in your leg or genitals, to the arteries in your heart. The dye makes the arteries visible on X-ray, revealing regions of blockage.</span><br /><br />

          <span><b>Cardiac Computed tomography (CT) or Magnetic resonance imaging (MRI)</b> - These tests create pictures of your heart and chest. Cardiac computed tomography (CT) scans use X-rays. Cardiac magnetic resonance imaging (MRI) uses a magnetic field and radio waves to produce pictures of your heart. For both tests, you lie on a table that slides inside a long tube-like machine. Each could be used to diagnose heart problems, including the degree of damage from heart attacks.</span><br /><br />
        </p><br><br>








        <p class="desc-big">Heart Attack Treatment</p>
        <p class="desc-normal"><b>Heart attack treatment at a hospital</b></p>
        <p class="desc-normal">Every minute after a heart attack, more heart tissue deteriorates or degenerates. Restoring blood flow quickly helps stop heart damage.</p><br>

        <p class="desc-normal"><b>Medications</b></p>
        <p class="desc-normal">Medications to treat a heart attack may include:</p>


        <p class="desc-normal blue_check">
            
          <span><b>Aspirin</b> - The 911 operator may tell you to take aspirin, or emergency medical personnel may give you aspirin immediately. Aspirin lowers blood clotting, therefore helping maintain blood flow through a narrowed artery.</span><br /><br />
          <span><b>Thrombolytics</b> - These drugs, also known as clot busters, help dissolve a blood clot that is stopping blood flow to your heart. The earlier you receive a thrombolytic drug after a heart attack, the greater the chance you will survive and have less heart damage.</span><br /><br />
          <span><b>Antiplatelet agents</b> - Emergency room primary care physicians might give you other drugs called platelet aggregation inhibitors to help prevent new clots and keep existing clots from getting larger.</span><br /><br />
          <span><b>Other blood-thinning medications</b> – You will likely be given other medications, for example, heparin, to make your blood less "sticky" and less likely to form clots. Heparin is given intravenous (IV) or by a syringe under your skin.</span><br /><br />
          <span><b>Pain relievers</b> - You may be given a pain reliever, for example, morphine.</span><br /><br />
          <span><b>Nitroglycerin</b> - This medication, used to treat chest pain (angina), could help improve blood flow to the heart by widening (dilating) the blood vessels.</span><br /><br />
          <span><b>Beta-blockers</b> - These medications help relax your heart muscle, slow your heartbeat and lower blood pressure, making your heart's job easier. Beta-blockers could restrict or limit the amount of heart muscle damage and prevent future heart attacks.</span><br /><br />
          <span><b>ACE inhibitors</b> - These drugs reduce blood pressure and lower stress on the heart.</span><br /><br />
          <span><b>Statins</b> - These drugs help to manage your blood cholesterol.</span><br /><br />
        </p><br><br>



        <img src="../../assets/img/heart-four.jpg" class="img-divider"><br><br>


        <p class="desc-big">Surgical and other procedures</p>
        <p class="desc-normal">In addition to medications, you may have one of these procedures to treat your heart attack:</p><br>


        <p class="desc-normal blue_check">
            
          <span><b>Coronary angioplasty and stenting</b> - In this procedure, also called percutaneous coronary intervention (PCI), primary care physicians guide a long, thin tube (catheter) through an artery in your genitals or wrist to a blocked artery in your heart. If you have had a heart attack, this procedure is usually done immediately after a cardiac catheterization, a procedure used to detect blockages.
          The catheter has a special balloon that, once in position, is briefly inflated to open an obstructed coronary artery. A metal mesh stent almost always is passed through the artery to keep it open long term, restoring blood flow to the heart. Generally, you get a stent coated with a slow-releasing medication to help keep your artery open.</span><br /><br />

          <span><b>Coronary artery bypass surgery</b> - In some cases, primary care physicians perform emergency bypass surgery at the time of a heart attack. If possible, although, you might have bypass surgery after your heart has had time about 3 to 7 days to recover from your heart attack. Bypass surgery involves sewing veins or arteries in place beyond an obstructed or narrowed coronary artery, permitting blood flow to the heart to bypass the narrowed section. You will likely remain in the hospital for some days after blood flow to your heart is restored and your condition is stable.</span><br /><br />
        </p><br><br>




        <p class="desc-big">Cardiac rehabilitation</p>
        <p class="desc-normal">Most hospitals provide programs that may begin while you are in the hospital and continue for weeks to a couple of months after you return home. Cardiac rehabilitation programs usually focus on 4 main areas — medications, lifestyle changes, emotional issues, and a gradual return to your routine activities.</p>
        <p class="desc-normal">It is extremely crucial to participate in this program. People who attend cardiac rehab after a heart attack usually live longer and are less likely to have another heart attack or complications from the heart attack. If cardiac rehab is not suggested during your hospitalization, ask your primary care physician about it.</p><br><br>




